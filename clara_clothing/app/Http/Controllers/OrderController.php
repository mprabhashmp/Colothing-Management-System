    {
        $id = $request->cus;

        $order = new Order();

        $order->cusid = $id;
        $order->products = $request->pro;
        $order->total = $request->tot;
        $order->pay = 0;
        $order->confirm = 0;

        $order->save();

        $deleted = DB::delete('delete from carts where cusid = ?',[$id]);

        return redirect()->route('order.index');
    }

